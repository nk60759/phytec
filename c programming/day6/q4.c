/*evaluate all relational operator*/
#include<stdio.h>
int main()
{
    int a,b;
    printf("enter no 1:");
    scanf("%d",&a);
    printf("enter no 2:");
    scanf("%d",&b);
    printf("%d\n",a==b);
    printf("%d\n",a!=b);
    printf("%d\n",a>b);
    printf("%d\n",a<b);
    printf("%d\n",a<=b);
    printf("%d\n",a>=b);
    return 0;
 
}